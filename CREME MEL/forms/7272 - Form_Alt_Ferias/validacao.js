function dataFormatada(dia,mes,ano){
	var mes = mes + 1;
    if (dia.toString().length == 1)
      dia = "0"+dia;
    if (mes.toString().length == 1)
      mes = "0"+mes;
    return dia+"/"+mes+"/"+ano;
}

